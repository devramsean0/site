var content = String(document.getElementById(`xml`).innerHTML)
content = content.replaceAll(`<sitemap>`, `<div class="url">`)
content = content.replaceAll(`</sitemap>`, `</div>`)
content = content.replaceAll(`<url>`, `<div class="url">`)
content = content.replaceAll(`</url>`, `</div>`)
content = content.replaceAll(`<loc>`, `<h1>`)
content = content.replaceAll(`</loc>`, `</h1>`)
content = content.replaceAll(`<lastmod>`, `<span class="lastmod">`)
content = content.replaceAll(`</lastmod>`, `</span><br/>`)
content = content.replaceAll(`<changefreq>`, `<span class="changefreq">`)
content = content.replaceAll(`</changefreq>`, `</span><br/>`)
content = content.replaceAll(`<priority>`, `<span class="priority">`)
content = content.replaceAll(`</priority>`, `</span><br/>`)
document.getElementById(`container`).innerHTML = content;